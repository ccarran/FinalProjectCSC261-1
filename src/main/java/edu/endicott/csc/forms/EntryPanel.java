    private void lblWebsiteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblWebsiteMouseClicked
        if (Desktop.isDesktopSupported() && Desktop.getDesktop().isSupported(Desktop.Action.BROWSE)) {
            try {
                Desktop.getDesktop().browse(new URI(Controller.getController().currentGameInfo.data.website));
            } catch (IOException ex) {
                Logger.getLogger(EntryPanel.class.getName()).log(Level.SEVERE, null, ex);
            } catch (URISyntaxException ex) {
                Logger.getLogger(EntryPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_lblWebsiteMouseClicked

