    private void dropdownScoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropdownScoreActionPerformed
        String selectedItem = (String) dropdownScore.getSelectedItem();
        
        if (("Select a Score").equals(selectedItem)) {
            selectedItem = "0";
        }
        
        try {
            Client.client.updateGameScoreUser(Controller.getController().user.username,
                    Controller.getController().currentGameInfo.data.steam_appid, selectedItem);
        } catch (IOException ex) {
            Logger.getLogger(EntryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_dropdownScoreActionPerformed


