    private void btnAddGameMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddGameMouseClicked
        try {
            Controller.getController().user =
                    Client.client.addGameToUser(
                            Controller.getController().username, 
                            Controller.getController().currentGameInfo.data.steam_appid)
                    .body;
        } catch (IOException ex) {
            Logger.getLogger(EntryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        btnAddGame.setEnabled(!Controller.getController()
                .user.gamelist.containsKey(
                        Controller.getController().currentGameInfo.data.steam_appid));
        btnRemoveGame.setEnabled(Controller.getController()
                .user.gamelist.containsKey(
                        Controller.getController().currentGameInfo.data.steam_appid));
        
        dropdownScore.setEnabled(!btnAddGame.isEnabled());
    }//GEN-LAST:event_btnAddGameMouseClicked

    private void btnRemoveGameMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveGameMouseClicked
        try {
            Controller.getController().user =
                    Client.client.removeGameFromUser(
                            Controller.getController().username, 
                            Controller.getController().currentGameInfo.data.steam_appid)
                    .body;
        } catch (IOException ex) {
            Logger.getLogger(EntryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        btnAddGame.setEnabled(!Controller.getController()
                .user.gamelist.containsKey(
                        Controller.getController().currentGameInfo.data.steam_appid));
        btnRemoveGame.setEnabled(Controller.getController()
                .user.gamelist.containsKey(
                        Controller.getController().currentGameInfo.data.steam_appid));
        
        dropdownScore.setSelectedIndex(0);
        dropdownScore.setEnabled(!btnAddGame.isEnabled());
    }//GEN-LAST:event_btnRemoveGameMouseClicked

