    public void updateInformation() {
        try {
            URL url = new URL(Controller.getController().currentGameInfo.data.header_image);
            BufferedImage buffImage = ImageIO.read(url);
            ImageIcon icon = new ImageIcon(buffImage);
            
            lblEntryImage.setIcon(icon);

        } catch (MalformedURLException ex) {
            Logger.getLogger(EntryPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(EntryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        lblEntryTitle.setText(Controller.getController().currentGameInfo.data.name);
        
        String genresString = "";
        if (Controller.getController().currentGameInfo.data.genres != null) {
            for (int i=0; i < Controller.getController().currentGameInfo.data.genres.size(); i++) {
                genresString = genresString + Controller.getController().currentGameInfo.data.genres.get(i).description;
                if (Controller.getController().currentGameInfo.data.genres.size() > 1 && i < Controller.getController().currentGameInfo.data.genres.size() - 1) {
                    genresString = genresString + ", ";
                }
            }
        }
        lblGenres.setText(genresString);
        
        jtaDescription.setText("Description:\n" + Controller.getController().currentGameInfo.data.short_description);
        lblReleaseDate.setText("Release date: " + Controller.getController().currentGameInfo.data.release_date.date);
        
        String publishersString = "Publishers: ";
        if (Controller.getController().currentGameInfo.data.publishers != null) {
            for (int i=0; i < Controller.getController().currentGameInfo.data.publishers.size(); i++) {
                publishersString = publishersString + Controller.getController().currentGameInfo.data.publishers.get(i);
                if (Controller.getController().currentGameInfo.data.publishers.size() > 1 && i < Controller.getController().currentGameInfo.data.publishers.size() - 1) {
                    publishersString = publishersString + ", ";
                }
            }
        }
        lblPublisher.setText(publishersString);
        
        String developersString = "Developers: ";
        if (Controller.getController().currentGameInfo.data.developers != null) {
            for (int i=0; i < Controller.getController().currentGameInfo.data.developers.size(); i++) {
                developersString = developersString + Controller.getController().currentGameInfo.data.developers.get(i);
                if (Controller.getController().currentGameInfo.data.developers.size() > 1 && i < Controller.getController().currentGameInfo.data.developers.size() - 1) {
                    developersString = developersString + ", ";
                }
            }
        }
        lblDeveloper.setText(developersString);
        
        lblWebsite.setText("Website: " + Controller.getController().currentGameInfo.data.website);
        
        if (Controller.getController().currentGameInfo.data.website == null) {
//            lblWebsite.setVisible(false);
            lblWebsite.setEnabled(false);
            lblWebsite.setText("");
        }
        else {
            lblWebsite.setVisible(true);
            lblWebsite.setEnabled(true);
        }
        
        btnAddGame.setEnabled(!Controller.getController().user.gamelist
                .containsKey(Controller.getController()
                        .currentGameInfo.data.steam_appid));
        btnRemoveGame.setEnabled(Controller.getController().user.gamelist
                .containsKey(Controller.getController()
                        .currentGameInfo.data.steam_appid));
        
        dropdownScore.setSelectedIndex(Controller.getController().user.gamelist
        .get(Controller.getController().currentGameInfo.data.steam_appid));
        
        dropdownScore.setEnabled(!btnAddGame.isEnabled());
    }
    
